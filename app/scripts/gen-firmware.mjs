import { readdirSync, writeFileSync, mkdirSync, statSync } from 'node:fs';
import { join, dirname, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Top-folder name → { manufacturer (display), arch }
const manufacturerMap = {
  'Seagate F3 Arch': { manufacturer: 'Seagate', arch: 'F3 Arch' },
  'WDC Marvell': { manufacturer: 'Western Digital', arch: 'Marvell' },
  'Sumsung HDD': { manufacturer: 'Samsung', arch: 'HDD' }, // folder is misspelled; display fixed
};

function listDirs(dir) {
  try {
    return readdirSync(dir, { withFileTypes: true }).filter((d) => d.isDirectory());
  } catch {
    return [];
  }
}

// Recursively collect files under `dir`; paths relative to `base`, forward-slash normalized.
function walkFiles(dir, base) {
  const out = [];
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return out;
  }
  for (const e of entries) {
    const full = join(dir, e.name);
    if (e.isDirectory()) {
      out.push(...walkFiles(full, base));
    } else if (e.isFile()) {
      const rel = relative(base, full).split(sep).join('/');
      let size = 0;
      try {
        size = statSync(full).size;
      } catch {
        /* unreadable — count as 0 */
      }
      out.push({ path: rel, size });
    }
  }
  return out;
}

// Full parse — returns families with per-donor file lists (used by tests + JSON snapshot).
export function parseFirmware(root) {
  const families = [];
  for (const top of listDirs(root)) {
    const meta = manufacturerMap[top.name];
    if (!meta) continue;
    for (const fam of listDirs(join(root, top.name))) {
      const donors = [];
      for (const donor of listDirs(join(root, top.name, fam.name))) {
        const donorRoot = join(root, top.name, fam.name, donor.name);
        const files = walkFiles(donorRoot, donorRoot);
        const totalBytes = files.reduce((sum, f) => sum + f.size, 0);
        donors.push({ name: donor.name, files, fileCount: files.length, totalBytes });
      }
      const familyBytes = donors.reduce((sum, d) => sum + d.totalBytes, 0);
      const fileCount = donors.reduce((sum, d) => sum + d.fileCount, 0);
      families.push({
        manufacturer: meta.manufacturer,
        arch: meta.arch,
        familyCode: fam.name,
        donors,
        donorCount: donors.length,
        fileCount,
        totalBytes: familyBytes,
        quarkUrl: '',
        quarkPwd: '',
      });
    }
  }
  families.sort(
    (a, b) => a.manufacturer.localeCompare(b.manufacturer) || a.familyCode.localeCompare(b.familyCode)
  );
  return families;
}

// "Key" firmware files = the identity/config files a DR engineer inspects
// (root-level files, any ROM path/file, DiskConfig/, known config extensions).
// Excludes bulk SA-module dumps (e.g. WD SABackup/*.rpm) — those are summarized as a count.
function isKeyFile(rel) {
  const lo = rel.toLowerCase();
  if (!lo.includes('/')) return true;
  if (lo.includes('rom')) return true;
  if (lo.startsWith('diskconfig/')) return true;
  if (/\.(ldd|rsf|fdd|cfg|img|sys)$/.test(lo)) return true;
  return false;
}

// Strip per-file paths for the bundled TS — keeps only donor names + counts + sizes.
// (The 289k file paths are emitted separately as lazy-loaded JSON, not inlined in the bundle.)
function toBundle(families) {
  return families.map((f) => ({
    manufacturer: f.manufacturer,
    arch: f.arch,
    familyCode: f.familyCode,
    donors: f.donors.map((d) => ({ name: d.name, fileCount: d.fileCount, totalBytes: d.totalBytes })),
    donorCount: f.donorCount,
    fileCount: f.fileCount,
    totalBytes: f.totalBytes,
    quarkUrl: f.quarkUrl,
    quarkPwd: f.quarkPwd,
  }));
}

// CLI entry — only when run directly, not when imported by the test
const isMain = process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1];
if (isMain) {
  const root = process.env.FIRMWARE_ROOT || 'N:/硬盘固件/UserDataFile';
  const outPath = process.env.FIRMWARE_OUT || join(__dirname, '..', 'src', 'data', 'firmware.ts');
  const families = parseFirmware(root);
  const bundle = toBundle(families);

  const byMfr = {};
  for (const f of bundle) byMfr[f.manufacturer] = (byMfr[f.manufacturer] || 0) + 1;
  const totalDonors = bundle.reduce((s, f) => s + f.donorCount, 0);
  const totalFiles = bundle.reduce((s, f) => s + f.fileCount, 0);
  const totalBytes = bundle.reduce((s, f) => s + f.totalBytes, 0);

  const body = `// AUTO-GENERATED by scripts/gen-firmware.mjs — do not edit by hand.
// Regenerate: npm run gen:firmware   (override source with FIRMWARE_ROOT)
// NOTE: aggregates only (no per-file paths). Per-donor file lists are a lazy JSON snapshot.
export interface FirmwareDonor {
  name: string;
  fileCount: number;
  totalBytes: number;
}

export interface FirmwareFamily {
  manufacturer: string;
  arch: string;
  familyCode: string;
  donors: FirmwareDonor[];
  donorCount: number;
  fileCount: number;
  totalBytes: number;
  quarkUrl: string;
  quarkPwd: string;
}

export const firmwareFamilies: FirmwareFamily[] = ${JSON.stringify(bundle, null, 2)};

export const firmwareStats: {
  total: number;
  totalDonors: number;
  totalFiles: number;
  totalBytes: number;
  byManufacturer: Record<string, number>;
} = {
  total: ${bundle.length},
  totalDonors: ${totalDonors},
  totalFiles: ${totalFiles},
  totalBytes: ${totalBytes},
  byManufacturer: ${JSON.stringify(byMfr, null, 2)},
};
`;
  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, body);

  // Per-family key-file snapshot — lazy-loaded by the firmware drill-down modal.
  // (Only "key" files; bulk SA modules are counted but not listed, to keep this small.)
  const snapPath = join(__dirname, '..', 'public', 'firmware-snapshot.json');
  mkdirSync(dirname(snapPath), { recursive: true });
  const snapshot = {};
  for (const f of families) {
    if (f.donorCount === 0) continue;
    snapshot[f.familyCode] = {
      manufacturer: f.manufacturer,
      arch: f.arch,
      donors: f.donors.map((d) => {
        const keyFiles = d.files.filter((x) => isKeyFile(x.path));
        return {
          name: d.name,
          keyFiles,
          keyCount: keyFiles.length,
          otherCount: d.files.length - keyFiles.length,
          totalBytes: d.totalBytes,
        };
      }),
    };
  }
  writeFileSync(snapPath, JSON.stringify(snapshot));

  console.log(
    `Generated ${bundle.length} families / ${totalDonors} donors / ${totalFiles} files (${totalBytes} bytes) → ${outPath}`,
  );
  console.log(`Emitted key-file snapshot → ${snapPath}`);
}
